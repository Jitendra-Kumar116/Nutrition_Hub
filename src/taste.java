
public class taste {

    double sweetness,
            saltiness,
            sourness,
            bitterness,
            savoriness,
            fattiness,
            spiciness;

    public taste(double sweetness, double saltiness, double sourness, double bitterness, double savoriness, double fattiness, double spiciness) {
        this.sweetness = sweetness;
        this.saltiness = saltiness;
        this.sourness = saltiness;
        this.bitterness = bitterness;
        this.savoriness = savoriness;
        this.fattiness = fattiness;
        this.spiciness = spiciness;
    }

}
